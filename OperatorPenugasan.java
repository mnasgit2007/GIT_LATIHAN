public class OperatorPenugasan {

    public static void main(String[] args) {
        // Deklarasi variabel a dengan nilai awal 10
        int a = 10;

        // Menggunakan operator penugasan +=
        a += 5;  // a = a + 5 -> a = 15

        // Menggunakan operator penugasan -=
        a -= 3;  // a = a - 3 -> a = 12

        // Menggunakan operator penugasan *=
        a *= 2;  // a = a * 2 -> a = 24

        // Menggunakan operator penugasan /=
        a /= 3;  // a = a / 3 -> a = 8

        // Menggunakan operator penugasan %=
        a %= 2;  // a = a % 2 -> a = 0
        // NILAI AKHIR a = 0

        // Mencetak nilai akhir a ke konsol
        System.out.println("Nilai akhir a: " + a);
    }
}
